
package com.gen.shopify.webhook.vo;



public class DiscountApplication {

    private String type;
    private String value;
    private String value_type;
    private String allocation_method;
    private String target_selection;
    private String target_type;
    private String description;
    private String title;

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the value
     */
    public String getValue() {
        return value;
    }

    /**
     * @param value the value to set
     */
    public void setValue(String value) {
        this.value = value;
    }

    /**
     * @return the value_type
     */
    public String getValue_type() {
        return value_type;
    }

    /**
     * @param value_type the value_type to set
     */
    public void setValue_type(String value_type) {
        this.value_type = value_type;
    }

    /**
     * @return the allocation_method
     */
    public String getAllocation_method() {
        return allocation_method;
    }

    /**
     * @param allocation_method the allocation_method to set
     */
    public void setAllocation_method(String allocation_method) {
        this.allocation_method = allocation_method;
    }

    /**
     * @return the target_selection
     */
    public String getTarget_selection() {
        return target_selection;
    }

    /**
     * @param target_selection the target_selection to set
     */
    public void setTarget_selection(String target_selection) {
        this.target_selection = target_selection;
    }

    /**
     * @return the target_type
     */
    public String getTarget_type() {
        return target_type;
    }

    /**
     * @param target_type the target_type to set
     */
    public void setTarget_type(String target_type) {
        this.target_type = target_type;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the title
     */
    public String getTitle() {
        return title;
    }

    /**
     * @param title the title to set
     */
    public void setTitle(String title) {
        this.title = title;
    }

}
